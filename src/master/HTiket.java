    private void btnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungActionPerformed
        // TODO add your handling code here:
        if (tHargaSatuTiket.getText().equals("") || tJumlahTiket.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Mohon masukkan harga tiket dan jumlahnya terlebih dahulu!!");
        } else {
            int ht = Integer.parseInt(tHargaSatuTiket.getText());
            int jt = Integer.parseInt(tJumlahTiket.getText());
            int totalharga = ht * jt;
            lTotalHarga.setText(Integer.toString(totalharga));
        }
    }//GEN-LAST:event_btnHitungActionPerformed


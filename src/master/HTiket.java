    public void tampilan(){
        Object [] baris = {"NO", "ID Tiket","ID Pesawat","ID Bandara Asal", "ID Bandara Tujuan", "Tanggal Keberangkatan", "Harga Satu Tiket", "Total Harga", "Jumlah Tiket",};
        tabMode = new DefaultTableModel(null, baris);
        tableTiket.setModel(tabMode);
        try {
                String sql = "SELECT * FROM tbl_tiket";
                st = conn.createStatement();
                rs = st.executeQuery(sql);
                int no = 0;
                while (rs.next()){
                        no++;
                        String id   = rs.getString("id");
                        String idp  = rs.getString("id_pesawat");
                        String idba = rs.getString("id_bandaraasal");
                        String idbj = rs.getString("id_bandaratujuan");
                        String t    = rs.getString("tanggal");
                        String h   = rs.getString("harga");
                        String th    = rs.getString("total_harga");
                        String j    = rs.getString("jumlah");

                        Object [] data = {no,id,idp,idba,idbj,t,h,th,j};
                        tabMode.addRow(data);
                }
        } catch (Exception e){
                System.out.println(e.toString());
        }
    }
    

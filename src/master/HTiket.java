    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        String hari = cmbHari.getSelectedItem().toString();
        String bulan = cmbBulan.getSelectedItem().toString();
        String tahun = cmbTahun.getSelectedItem().toString();
        String tanggal = tahun+"-"+bulan+"-"+hari;
        
        String id_pesawat = cmbIDPesawat.getSelectedItem().toString();
        String id_bandaraasal = cmbIDBandaraAsal.getSelectedItem().toString();
        String id_bandaratujuan = cmbIDBandaraTujuan.getSelectedItem().toString();
        
        if (tIDTiket.getText().equals("") || tanggal == "" || tHargaSatuTiket.getText().equals("") || tJumlahTiket.getText().equals("") ){
            JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!!");
        } else {
            try {
               int s;
               String sql = "INSERT INTO tbl_tiket VALUES ('"+tIDTiket.getText()+"', '"+id_pesawat+"', '"+id_bandaraasal+"'  , '"+id_bandaratujuan+"'  , '"+tanggal+"', '"+tHargaSatuTiket.getText()+"', '"+lTotalHarga.getText()+"', '"+tJumlahTiket.getText()+"')";
               st = conn.createStatement();
               s = st.executeUpdate(sql);
               if (s == 1){
                JOptionPane.showMessageDialog(null, "Berhasil disimpan");
                clear();
                tampilan();
               }
           } catch (Exception e){
                System.out.println(e.toString());
           }    
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUbahActionPerformed
        // TODO add your handling code here:
        String hari = cmbHari.getSelectedItem().toString();
        String bulan = cmbBulan.getSelectedItem().toString();
        String tahun = cmbTahun.getSelectedItem().toString();
        String tanggal = tahun+"-"+bulan+"-"+hari;
        
        String id_pesawat = cmbIDPesawat.getSelectedItem().toString();
        String id_bandaraasal = cmbIDBandaraAsal.getSelectedItem().toString();
        String id_bandaratujuan = cmbIDBandaraTujuan.getSelectedItem().toString();
        
        int c = JOptionPane.showConfirmDialog(null, "Apakah anda ingin mengubah data "+tIDTiket.getText()+" ?" ,"Informasi", JOptionPane.YES_NO_OPTION);
        if (c == JOptionPane.YES_OPTION){
            if (tIDTiket.getText().equals("") || tanggal == ""){
                JOptionPane.showMessageDialog(null, "Data tidak boleh kosong!!");
            } else {
                try {
                   int s;
                   String sql = "UPDATE tbl_tiket SET id_pesawat = '"+id_pesawat+"', id_bandaraasal = '"+id_bandaraasal+"'  , id_bandaratujuan = '"+id_bandaratujuan+"'  , tanggal = '"+tanggal+"' , harga = '"+tHargaSatuTiket.getText()+"' , total_harga = '"+lTotalHarga.getText()+"' , jumlah = '"+tJumlahTiket.getText()+"' WHERE id = '"+tIDTiket.getText()+"' ";
                   st = conn.createStatement();
                   s = st.executeUpdate(sql);
                   if (s == 1){
                    JOptionPane.showMessageDialog(null, "Berhasil diubah");
                    clear();
                    tampilan();
                   }
               } catch (Exception e){
                    System.out.println(e.toString());
               }    
            }
        }
    }//GEN-LAST:event_btnUbahActionPerformed

